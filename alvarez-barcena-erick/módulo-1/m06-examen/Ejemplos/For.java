public class For {
    public static void main(String[] args) {
        //Se crea la estructura for
        //El primer parametro es una variable de control, inicia en 1.
        //El segundo parametro es una expresión relacional que
        //para cada vez que se cumpla se ejecutará el código dentro del ciclo.
        //El tercer parametro es un incremento.
        for (int i = 1; i <= 5; i++) {
            System.out.println(i*2);
        }
    }
}
